#!/usr/bin/env python3
"""
RAWES Simulation Mediator
Bridges ArduPilot SITL <-> Python RK4 dynamics for end-to-end flight simulation.

Architecture:
    ArduPilot SITL  <--UDP 9002/9003-->  mediator.py  <--function call-->  dynamics.py (RK4)

Data flow (each 400 Hz step):
    1. recv_servos()     — non-blocking, from SITL UDP port 9002
    2. swashplate mix    — servo PWM -> collective + cyclic tilt
    3. aero.compute()    — BEM forces in world (ENU) frame
    4. tether.compute()  — tension-only tether force added to wrench
    5. dynamics.step()   — RK4 6-DOF integration (pure Python, gravity internal)
    6. sensor packet     — minimal NED state for ACRO mode (gyro + constant gravity)
    7. send_state()      — to SITL UDP port 9003

Usage:
    python3 mediator.py [options]

Options:
    --sitl-recv-port  PORT     SITL servo recv port  (default: 9002)
    --sitl-send-port  PORT     SITL state send port  (default: 9003)
    --wind-x  FLOAT            Initial wind E [m/s]  (default: 10.0)
    --wind-y  FLOAT            Initial wind N [m/s]  (default: 0.0)
    --wind-z  FLOAT            Initial wind U [m/s]  (default: 0.0)
    --log-level LEVEL          Logging level         (default: INFO)
"""

import argparse
import csv
import logging
import sys
import time
import os

import numpy as np

# Local modules (same directory)
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from dynamics        import RigidBodyDynamics
from sitl_interface  import SITLInterface
from swashplate      import h3_inverse_mix, collective_to_pitch, cyclic_to_blade_pitches, pwm_to_normalized
from aero            import RotorAero
from tether          import TetherModel
from frames          import T_ENU_NED, build_orb_frame
from sensor          import build_sitl_packet, SensorSim

# ---------------------------------------------------------------------------
# Configuration defaults
# ---------------------------------------------------------------------------
DT_TARGET    = 1.0 / 400.0    # 400 Hz target loop rate [s]
LOG_INTERVAL = 1.0             # position/attitude log interval [s]
WEIGHT_N     = 294.3           # rotor weight [N] = 30 kg * 9.81 m/s²
I_SPIN_KGMS2 = 10.0            # rotor spin-axis moment of inertia [kg·m²]
OMEGA_SPIN_MIN = 0.5           # minimum spin rate clamp [rad/s]

# Spin torque model: Q_net = K_DRIVE * v_inplane − K_DRAG * omega²
#
# The BEM torque integral (Q_positive/Q_negative) cannot model the spin
# equilibrium correctly: because thrust scales as omega², the drive term also
# scales as omega², so drive always exceeds drag and omega runs away without
# limit.  Instead we use a physically-motivated two-term model:
#
#   Q_drive  = K_DRIVE_SPIN × v_inplane   (energy extracted from lateral wind,
#                                           constant for a given wind speed)
#   Q_drag   = K_DRAG_SPIN  × omega²      (profile + induced drag, ∝ rpm²)
#
# These have different omega-dependence, so they balance at a stable
# equilibrium omega_eq = sqrt(K_DRIVE × v_inplane / K_DRAG).
#
# Tuning (at design point: v_wind = 10 m/s → omega_eq = 28 rad/s, λ = 7):
#   K_DRIVE_SPIN × 10  =  K_DRAG_SPIN × 28²
#   Choose K_DRAG_SPIN so that at equilibrium the torque magnitude is ≈ 14 N·m
#   (plausible for a 5 kg, 2.5 m blade rotor):
#       K_DRAG_SPIN  = 14 / 28²  ≈ 0.01786 N·m·s²/rad²
#       K_DRIVE_SPIN = 0.01786 × 28² / 10 = 1.4 N·m·s/m
#
# At v = 20 m/s:  omega_eq = sqrt(1.4 × 20 / 0.01786) ≈ 39.6 rad/s  (higher wind → faster spin)
K_DRIVE_SPIN = 1.4      # N·m·s/m   (autorotation drive per unit in-plane wind speed)
K_DRAG_SPIN  = 0.01786  # N·m·s²/rad²  (profile drag coefficient)

# Default initial state — warmup-settled equilibrium from test_steady_flight.py.
# 50 m tether at ~30° elevation (ENU), body Z aligned with tether direction.
# Generated by running: pytest simulation/tests/unit/test_steady_flight.py
DEFAULT_POS0        = [46.258, 14.241, 12.530]   # ENU [m]
DEFAULT_VEL0        = [-0.257,  0.916, -0.093]   # ENU [m/s]
DEFAULT_BODY_Z      = [0.851018, 0.305391, 0.427206]  # unit vector (body Z = axle direction)
DEFAULT_OMEGA_SPIN  = 20.148                      # rad/s — equilibrium spin at 30° elevation



# ---------------------------------------------------------------------------
# Argument parsing
# ---------------------------------------------------------------------------
def _parse_args():
    parser = argparse.ArgumentParser(
        description="RAWES Simulation Mediator — bridges ArduPilot SITL and Python RK4 dynamics",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--sitl-recv-port", type=int, default=9002,
                        help="UDP port to listen on for SITL servo data")
    parser.add_argument("--sitl-send-port", type=int, default=9003,
                        help="UDP port to send physics state to SITL")
    parser.add_argument("--wind-x", type=float, default=10.0,
                        help="Ambient wind East component [m/s]")
    parser.add_argument("--wind-y", type=float, default=0.0,
                        help="Ambient wind North component [m/s]")
    parser.add_argument("--wind-z", type=float, default=0.0,
                        help="Ambient wind Up component [m/s]")
    parser.add_argument("--log-level", default="INFO",
                        choices=["DEBUG", "INFO", "WARNING", "ERROR"],
                        help="Logging verbosity level")
    parser.add_argument("--telemetry-log", default=None,
                        help="Path for per-step CSV telemetry log (MBDyn + aero data)")
    parser.add_argument("--tether-rest-length", type=float, default=49.949,
                        help="Unstretched tether length [m] (ground-winch pay-out). "
                             "Default 49.949 m chosen for near-zero force balance at the "
                             "warmup equilibrium: T≈289N → net vertical ≈-2.6N vs gravity=49N.")
    parser.add_argument("--startup-freeze-seconds", type=float, default=30.0,
                        help="Seconds to immobilize hub at startup (pos/R fixed, vel=0) "
                             "so ArduPilot EKF can achieve GPS lock.  The rotor still "
                             "spins freely in the wind during this period.  After this "
                             "period normal 6-DOF dynamics begin.")
    parser.add_argument("--lock-orientation", action="store_true", default=False,
                        help="Lock hub orientation to the initial R0 every step — "
                             "no rotation allowed (magic tether that prevents all twist). "
                             "Eliminates rotational dynamics so only translational physics "
                             "needs to be validated.  Swashplate is always aligned correctly; "
                             "anti-rotation motor torque is irrelevant.")
    parser.add_argument("--pos0", type=str, default=None,
                        help="Initial hub ENU position as 'x,y,z' [m]. "
                             "Overrides built-in default.")
    parser.add_argument("--vel0", type=str, default=None,
                        help="Initial hub ENU velocity as 'x,y,z' [m/s].")
    parser.add_argument("--body-z", type=str, default=None,
                        help="Initial body-Z axis in world frame as 'x,y,z' (unit vector). "
                             "R0 is constructed so that column 2 = body-z.")
    parser.add_argument("--omega-spin", type=float, default=None,
                        help="Initial rotor spin rate [rad/s].")
    return parser.parse_args()


# ---------------------------------------------------------------------------
# Main mediator loop
# ---------------------------------------------------------------------------
def run_mediator(args):
    log = logging.getLogger("mediator")

    wind_world = np.array([args.wind_x, args.wind_y, args.wind_z])
    log.info("Wind vector (ENU): %s m/s", wind_world)

    # -- Instantiate subsystems -----------------------------------------------
    # Mass 30 kg: the blade geometry (4 × 2 m, chord 0.15 m) was scaled from
    # the De Schutter (2018) 40 kg reference design.  At the design tip-speed
    # ratio (λ = 7, v_wind = 10 m/s → ω = 28 rad/s) the BEM model produces
    # ~300 N of thrust at neutral collective — consistent with a ~30 kg rotor
    # hovering near its natural autorotation equilibrium.  Using 5 kg (actual
    # hardware mass) causes 6× over-thrust and makes the altitude controller
    # saturate.  TODO: either retune the blade geometry to the actual mass, or
    # verify the hardware mass is correct (5 kg seems very light for this blade
    # area and tip speed).
    # I_spin=0: do NOT include rotor gyroscopic coupling in the orbital dynamics.
    # The lumped single-body model uses ArduPilot's cyclic commands directly as
    # tilting moments (no blade flapping model).  The 90° gyroscopic precession
    # that a real spinning rotor would produce is already compensated by the
    # swashplate phase angle in a real helicopter; adding it here without the
    # corresponding phase compensation would cause ArduPilot's attitude
    # controller to tilt the disk in the wrong direction.
    # ── Initial state ──────────────────────────────────────────────────────────
    # Defaults: warmup-settled equilibrium from test_steady_flight.py
    #   (50 m tether at ~30° elevation, body Z aligned with tether)
    _pos0            = list(DEFAULT_POS0)
    _vel0            = list(DEFAULT_VEL0)
    _omega_spin_init = DEFAULT_OMEGA_SPIN

    if args.pos0 is not None:
        _pos0 = [float(v) for v in args.pos0.split(",")]
    if args.vel0 is not None:
        _vel0 = [float(v) for v in args.vel0.split(",")]
    if args.omega_spin is not None:
        _omega_spin_init = args.omega_spin

    # body_z sets the initial hub orientation (rotor axle direction).
    # Warmup body_z = DEFAULT_BODY_Z ≈ [0.851,0.305,0.427] has z=0.427:
    # vertical thrust = T × 0.427 ≈ 128 N, balancing gravity+tether_down ≈ 126 N.
    _pos0_arr = np.array(_pos0, dtype=float)
    if args.body_z is not None:
        _bz_raw = np.array([float(v) for v in args.body_z.split(",")], dtype=float)
    else:
        _bz_raw = np.array(DEFAULT_BODY_Z, dtype=float)
    _bz = _bz_raw / np.linalg.norm(_bz_raw)

    # Build _R0: body-X is the East-projection of _bz.
    _R0 = build_orb_frame(_bz)

    dynamics = RigidBodyDynamics(
        mass   = 5.0,
        I_body = [5.0, 5.0, 10.0],    # matches test_steady_flight.py warmup model
        I_spin = 0.0,                  # no gyroscopic coupling (see comment above)
        pos0   = _pos0,
        vel0   = _vel0,
        R0     = _R0,
        omega0  = [0.0, 0.0, 0.0],    # orbital angular velocity only (no spin)
        z_floor = 1.0,                # safety net only — prevent underground; startup freeze handles pre-arm
    )
    sitl   = SITLInterface(
        recv_port=args.sitl_recv_port,
        send_port=args.sitl_send_port,
    )
    aero   = RotorAero()
    tether = TetherModel(
        anchor_enu             = np.array([0.0, 0.0, 0.0]),
        rest_length            = args.tether_rest_length,
        hub_mass               = 5.0,   # matches dynamics mass; sets critical damping scale
        axle_attachment_length = 0.0,   # disable restoring torque; body_z stability via aero
    )
    # SensorSim reports tether-relative attitude (R_dev vs tether direction).
    # Using live tether direction (stable_body_z=None) so roll=pitch=0 whenever
    # body_z is aligned with the current tether direction, and non-zero otherwise.
    # This gives compute_rc_from_attitude a real attitude-error signal.
    sensor_sim = SensorSim(
        home_enu_z    = float(_pos0[2]),
        anchor_enu    = np.zeros(3),
        stable_body_z = None,
    )
    log.info(
        "Tether: Dyneema SK75 1.9mm  EA=%.0f kN  rest_length=%.0f m  "
        "damping=%.1f N·s/m  break_load=%.0f N",
        tether.EA / 1000, tether.rest_length,
        tether.damping, tether.BREAK_LOAD_N,
    )

    # -- Connect ---------------------------------------------------------------
    log.info("Binding SITL UDP sockets...")
    sitl.bind()
    log.info("Python RigidBodyDynamics ready. Starting main loop.")

    # -- State ----------------------------------------------------------------
    t_sim           = 0.0
    step            = 0
    last_log_time   = -LOG_INTERVAL       # ensure immediate first log
    last_vel        = np.zeros(3)
    hub_state       = {
        "pos":   np.array(_pos0, dtype=float),
        "vel":   np.array(_vel0, dtype=float),
        "R":     _R0.copy(),
        "omega": np.array([0.0, 0.0, 0.0]),
    }
    last_servos   = np.zeros(16)   # neutral PWM (mapped from 1500µs → 0)
    omega_spin    = _omega_spin_init
    startup_freeze = getattr(args, 'startup_freeze_seconds', 30.0)

    # -- Telemetry CSV --------------------------------------------------------
    _telemetry_file   = None
    _telemetry_writer = None
    if args.telemetry_log:
        _telemetry_file = open(args.telemetry_log, "w", newline="", encoding="utf-8")
        _telemetry_writer = csv.writer(_telemetry_file)
        # CSV schema (see also: analysis/generate_flight_report.py for column usage)
        _telemetry_writer.writerow([
            # Time
            "t_sim",
            # Servo inputs (normalized, -1..+1)
            "servo_s1", "servo_s2", "servo_s3", "servo_esc",
            # Swashplate mixing outputs
            "collective_norm", "tilt_lon", "tilt_lat", "collective_rad",
            # Rotor spin estimate
            "omega_rotor",
            # Aerodynamic model internals
            "aero_T", "aero_v_axial", "aero_v_i", "aero_v_inplane", "aero_ramp",
            "aero_Q_drag", "aero_Q_drive",
            # Aerodynamic forces sent to MBDyn (ENU world frame)
            "F_x", "F_y", "F_z", "M_x", "M_y", "M_z",
            # Hub state received from MBDyn (ENU world frame)
            "hub_pos_x", "hub_pos_y", "hub_pos_z",
            "hub_vel_x", "hub_vel_y", "hub_vel_z",
            "hub_omega_x", "hub_omega_y", "hub_omega_z",
            # Hub acceleration (finite-difference)
            "hub_accel_x", "hub_accel_y", "hub_accel_z",
            # Tether state
            "tether_length", "tether_extension", "tether_tension",
            "tether_fx", "tether_fy", "tether_fz",
            "tether_slack",
            # Tether-relative attitude sent to ArduPilot [rad]
            "rpy_roll", "rpy_pitch", "rpy_yaw",
        ])
        log.info("Telemetry logging → %s", args.telemetry_log)

    # -- Main loop ------------------------------------------------------------
    log.info("Entering main loop at %.0f Hz target.", 1.0 / DT_TARGET)

    try:
        t_wall_start = time.monotonic()

        while True:
            t_wall_loop = time.monotonic()
            t_sim = t_wall_loop - t_wall_start

            # ----------------------------------------------------------------
            # Step 1: Receive servo values from SITL (non-blocking)
            # ----------------------------------------------------------------
            new_servos = sitl.recv_servos()
            if new_servos is not None:
                last_servos = new_servos
            # Use last known servo values if no new packet
            servos = last_servos

            # ── Startup immobilization ────────────────────────────────────────
            # Hold hub position and orientation fixed (axle anchored to a solid
            # point) while the EKF achieves GPS lock.  The rotor still spins
            # freely: we run aero with v_hub=0 so wind-driven torque brings
            # omega_spin to its natural equilibrium before physics starts.
            # ─────────────────────────────────────────────────────────────────
            if t_sim < startup_freeze:
                _imm_forces = aero.compute_forces(
                    collective_rad = 0.0,
                    tilt_lon       = 0.0,
                    tilt_lat       = 0.0,
                    R_hub          = hub_state["R"],
                    v_hub_world    = np.zeros(3),   # hub stationary
                    omega_rotor    = omega_spin,
                    wind_world     = wind_world,
                    t              = t_sim,
                )
                Q_spin = (K_DRIVE_SPIN * aero.last_v_inplane
                          - K_DRAG_SPIN  * omega_spin ** 2)
                omega_spin = max(OMEGA_SPIN_MIN,
                                 omega_spin + Q_spin / I_SPIN_KGMS2 * DT_TARGET)
                # Send the initial velocity as a hint so the EKF can align yaw
                # before physics starts.  Without this, the EKF never sees enough
                # velocity to complete yaw alignment when the tether is well-damped
                # (hub barely moves after freeze ends → EKF stalls at origin-set).
                # Using _vel0 (the velocity at physics start) avoids a discontinuity.
                sensor_data = sensor_sim.compute(
                    pos_enu         = hub_state["pos"],
                    vel_enu         = _vel0,
                    R_hub           = hub_state["R"],
                    omega_body      = np.zeros(3),
                    accel_world_enu = np.zeros(3),
                    dt              = DT_TARGET,
                )
                sitl.send_state(
                    timestamp  = t_sim,
                    pos_ned    = sensor_data["pos_ned"],
                    vel_ned    = sensor_data["vel_ned"],
                    rpy_rad    = sensor_data["rpy"],
                    accel_body = sensor_data["accel_body"],
                    gyro_body  = sensor_data["gyro_body"],
                    rpm_rad_s  = omega_spin,
                )
                if t_sim - last_log_time >= LOG_INTERVAL:
                    last_log_time = t_sim
                    log.info(
                        "t=%6.1fs  [IMMOBILIZED — EKF startup]  "
                        "omega=%.1f rad/s  remaining=%.0fs",
                        t_sim, omega_spin, startup_freeze - t_sim,
                    )
                elapsed = time.monotonic() - t_wall_loop
                remaining = DT_TARGET - elapsed
                if remaining > 0:
                    time.sleep(remaining)
                step += 1
                continue

            # ── Normal physics path (after immobilization) ────────────────────
            # ----------------------------------------------------------------
            # Step 2: Swashplate mixing
            # Servo channels 0,1,2 = S1,S2,S3 (swashplate); channel 3 = ESC
            # Servos arrive normalised [-1,1] from SITLInterface
            # ----------------------------------------------------------------
            s1 = float(servos[0])
            s2 = float(servos[1])
            s3 = float(servos[2])
            esc_norm = float(np.clip(servos[3], -1.0, 1.0))

            collective_norm, tilt_lon, tilt_lat = h3_inverse_mix(s1, s2, s3)
            collective_rad = collective_to_pitch(collective_norm)

            # ----------------------------------------------------------------
            # Step 3: Compute aerodynamic forces
            # Use current hub state from previous MBDyn step
            # ----------------------------------------------------------------
            R_hub = hub_state["R"]
            v_hub = hub_state["vel"]

            forces = aero.compute_forces(
                collective_rad = collective_rad,
                tilt_lon       = tilt_lon,
                tilt_lat       = tilt_lat,
                R_hub          = R_hub,
                v_hub_world    = v_hub,
                omega_rotor    = omega_spin,   # separate spin state — never decays unexpectedly
                wind_world     = wind_world,
                t              = t_sim,
            )

            # ----------------------------------------------------------------
            # Step 4: Tether force (tension-only, Dyneema SK75 1.9 mm)
            # Computed in Python so we can enforce the slack condition
            # (a real rope cannot push).  Added to aerodynamic force before
            # sending to MBDyn.
            # ----------------------------------------------------------------
            tether_force, tether_moment = tether.compute(
                hub_state["pos"], hub_state["vel"], hub_state["R"])
            forces[0:3] += tether_force    # add to Fx/Fy/Fz
            forces[3:6] += tether_moment   # restoring torque aligns axle with tether

            # Warn if tether tension approaches break load
            if tether._last_info.get("tension", 0) > 0.8 * tether.BREAK_LOAD_N:
                log.warning(
                    "t=%.1f TETHER TENSION %.0f N is >80%% of break load (%.0f N)!",
                    t_sim, tether._last_info["tension"], tether.BREAK_LOAD_N,
                )

            # ----------------------------------------------------------------
            # Step 4b: Thrust magnitude (for logging)
            # ----------------------------------------------------------------
            disk_normal = R_hub[:, 2]
            T_est = max(0.0, float(np.dot(forces[0:3], disk_normal)))

            # ----------------------------------------------------------------
            # Step 4c: Update omega_spin from autorotation torque balance
            #
            # Q_drive ∝ v_inplane (wind energy extraction — does NOT depend on
            #   omega, so it has a different omega-scaling than Q_drag).
            # Q_drag  ∝ omega²   (profile drag — always opposes rotation).
            #
            # This gives a stable equilibrium omega_eq = sqrt(K_DRIVE × v_inplane / K_DRAG).
            # The BEM Q_drive/Q_drag terms are NOT used here because the BEM
            # torque also scales as omega² (through dynamic pressure), making
            # drive and drag parallel — no equilibrium exists and omega runs away.
            Q_spin = (K_DRIVE_SPIN * aero.last_v_inplane
                      - K_DRAG_SPIN  * omega_spin ** 2)
            omega_spin = max(OMEGA_SPIN_MIN,
                             omega_spin + Q_spin / I_SPIN_KGMS2 * DT_TARGET)

            # ----------------------------------------------------------------
            # Step 5: Integrate rigid-body dynamics (RK4, gravity internal)
            # forces[0:3] = aero + tether force
            # M_orbital   = cyclic moments only (M_spin excluded — it updates omega_spin above)
            # omega_spin  passed for gyroscopic coupling in Euler equations
            # Do NOT add gravity here — dynamics.py applies it internally.
            # ----------------------------------------------------------------
            M_orbital = forces[3:6] - aero.last_M_spin

            if args.lock_orientation:
                # Magic tether: no rotation allowed.  Pass zero moments so the
                # integrator never accelerates angular velocity, then forcibly
                # reset R and omega after the step to eliminate numerical drift.
                M_step = np.zeros(3)
            else:
                # Attitude rate damping: oppose orbital angular velocity to prevent
                # free tumbling.  Without gyroscopic coupling (I_spin=0) any cyclic
                # moment accelerates hub attitude without bound.  A viscous damping
                # term M_damp = -c * omega_orbital mimics aerodynamic blade-flapping
                # damping that a real rotor provides naturally.  The value ~2 N·m·s
                # gives a time constant τ = I/c = 5/2 = 2.5 s — slow enough that
                # ArduPilot's attitude loop (~100 Hz) easily commands through it,
                # fast enough to prevent runaway tumbling.
                M_orbital += -50.0 * hub_state["omega"]
                M_step = M_orbital

            prev_vel  = hub_state["vel"].copy()
            hub_state = dynamics.step(forces[0:3], M_step, DT_TARGET,
                                      omega_spin=omega_spin)
            new_vel   = hub_state["vel"]

            if args.lock_orientation:
                # Track current tether direction: body_z = normalize(hub_pos).
                # This keeps the rotor disk aligned with the tether as the hub
                # orbits, while eliminating rotational tumbling (omega=0).
                # Locking to the INITIAL direction (_R0) was wrong: thrust would
                # stop being radial as the hub orbited, decelerating the orbit
                # via aero drag until the hub came to rest.
                _cur_body_z = hub_state["pos"] / np.linalg.norm(hub_state["pos"])
                hub_state["R"]     = build_orb_frame(_cur_body_z)
                hub_state["omega"] = np.zeros(3)

            # ----------------------------------------------------------------
            # Step 7: Compute hub acceleration (finite difference)
            # ----------------------------------------------------------------
            accel_world = (new_vel - prev_vel) / DT_TARGET

            # ----------------------------------------------------------------
            # Step 8: Build SITL sensor packet
            # SensorSim.compute() reports tether-relative roll/pitch so that
            # compute_rc_from_attitude has a real error signal.
            # ----------------------------------------------------------------
            sensor_data = sensor_sim.compute(
                pos_enu         = hub_state["pos"],
                vel_enu         = hub_state["vel"],
                R_hub           = hub_state["R"],
                omega_body      = hub_state["omega"],
                accel_world_enu = accel_world,
                dt              = DT_TARGET,
            )

            # ----------------------------------------------------------------
            # Step 8b: Write telemetry row (after sensor so rpy is available)
            # ----------------------------------------------------------------
            if _telemetry_writer is not None:
                _rpy = sensor_data["rpy"]
                _telemetry_writer.writerow([
                    f"{t_sim:.6f}",
                    f"{s1:.6f}", f"{s2:.6f}", f"{s3:.6f}", f"{esc_norm:.6f}",
                    f"{collective_norm:.6f}", f"{tilt_lon:.6f}", f"{tilt_lat:.6f}",
                    f"{collective_rad:.6f}",
                    f"{omega_spin:.4f}",
                    f"{aero.last_T:.4f}",
                    f"{aero.last_v_axial:.4f}",
                    f"{aero.last_v_i:.4f}",
                    f"{aero.last_v_inplane:.4f}",
                    f"{aero.last_ramp:.4f}",
                    f"{aero.last_Q_drag:.4f}",
                    f"{aero.last_Q_drive:.4f}",
                    f"{forces[0]:.4f}", f"{forces[1]:.4f}", f"{forces[2]:.4f}",
                    f"{forces[3]:.4f}", f"{forces[4]:.4f}", f"{forces[5]:.4f}",
                    f"{hub_state['pos'][0]:.4f}", f"{hub_state['pos'][1]:.4f}", f"{hub_state['pos'][2]:.4f}",
                    f"{hub_state['vel'][0]:.4f}", f"{hub_state['vel'][1]:.4f}", f"{hub_state['vel'][2]:.4f}",
                    f"{hub_state['omega'][0]:.4f}", f"{hub_state['omega'][1]:.4f}", f"{hub_state['omega'][2]:.4f}",
                    f"{accel_world[0]:.4f}", f"{accel_world[1]:.4f}", f"{accel_world[2]:.4f}",
                    f"{tether._last_info.get('length', 0.0):.4f}",
                    f"{tether._last_info.get('extension', 0.0):.4f}",
                    f"{tether._last_info.get('tension', 0.0):.4f}",
                    f"{tether_force[0]:.4f}", f"{tether_force[1]:.4f}", f"{tether_force[2]:.4f}",
                    "1" if tether._last_info.get("slack", True) else "0",
                    f"{_rpy[0]:.6f}", f"{_rpy[1]:.6f}", f"{_rpy[2]:.6f}",
                ])

            # ----------------------------------------------------------------
            # Step 9: Send physics state to SITL
            # ----------------------------------------------------------------
            sitl.send_state(
                timestamp  = t_sim,
                pos_ned    = sensor_data["pos_ned"],
                vel_ned    = sensor_data["vel_ned"],
                rpy_rad    = sensor_data["rpy"],
                accel_body = sensor_data["accel_body"],
                gyro_body  = sensor_data["gyro_body"],
                rpm_rad_s  = omega_spin,
            )

            # ----------------------------------------------------------------
            # Step 10: Periodic status log (1 Hz)
            # ----------------------------------------------------------------
            if t_sim - last_log_time >= LOG_INTERVAL:
                last_log_time = t_sim
                pos   = hub_state["pos"]
                rpy_d = np.degrees(sensor_data["rpy"])
                alt   = pos[2]
                teth = tether._last_info
                teth_str = (
                    f"SLACK  L={teth.get('length',0):.1f}m"
                    if teth.get("slack", True) else
                    f"TAUT  T={teth.get('tension',0):.0f}N  ext={teth.get('extension',0):.3f}m"
                )
                log.info(
                    "t=%6.1fs  pos_ENU=[%6.1f %6.1f %6.1f]m  "
                    "rpy=[%5.1f %5.1f %5.1f]deg  "
                    "omega=%.1f rad/s  T=%.1fN  tether=%s",
                    t_sim,
                    pos[0], pos[1], pos[2],
                    rpy_d[0], rpy_d[1], rpy_d[2],
                    omega_spin,
                    T_est,
                    teth_str,
                )

            # ----------------------------------------------------------------
            # Timing: sleep remainder of target period
            # ----------------------------------------------------------------
            elapsed = time.monotonic() - t_wall_loop
            remaining = DT_TARGET - elapsed
            if remaining > 0:
                time.sleep(remaining)

            step += 1

    except KeyboardInterrupt:
        log.info("KeyboardInterrupt received — shutting down.")

    except Exception as exc:
        log.exception("Fatal error in main loop: %s", exc)
        raise

    finally:
        # -- Graceful shutdown ------------------------------------------------
        log.info("Final state at step %d (t=%.2f s):", step, t_sim)
        log.info("  pos_ENU : %s m",  hub_state["pos"].round(3))
        log.info("  vel_ENU : %s m/s", hub_state["vel"].round(3))
        log.info("  omega_spin : %.2f rad/s", omega_spin)

        if _telemetry_file is not None:
            try:
                _telemetry_file.flush()
                _telemetry_file.close()
                log.info("Telemetry log closed.")
            except Exception:
                pass

        log.info("Closing connections ...")
        try:
            sitl.close()
        except Exception:
            pass
        log.info("Mediator stopped.")


# ---------------------------------------------------------------------------
