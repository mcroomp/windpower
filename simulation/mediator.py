def main():
    args = _parse_args()
    logging.basicConfig(
        level    = getattr(logging, args.log_level),
        format   = "%(asctime)s %(levelname)-8s %(name)s: %(message)s",
        datefmt  = "%H:%M:%S",
    )
    run_mediator(args)


if __name__ == "__main__":
    main()
